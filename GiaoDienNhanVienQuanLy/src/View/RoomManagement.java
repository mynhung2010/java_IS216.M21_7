    private void loadData() throws SQLException{
        PreparedStatement pstm = null;
        ResultSet rs = null;
        try {
            Connection conn = ConnectionUtils.getMyConnection();
            rs = Room.getFullData();
            tbThongTinPhong.removeAll();
            String arr[] = {"Mã phòng", "Loại phòng", "Chất lượng","Giá"};
            DefaultTableModel dtm = new DefaultTableModel(arr,0);
            while(rs.next()){
                Vector vt = new Vector();
                vt.add(rs.getString("RoomNo"));
                vt.add(rs.getString("TypeOfRoom"));
                vt.add(rs.getString("Quality"));
                vt.add(rs.getString("Cost"));
                dtm.addRow(vt);
            }
            tbThongTinPhong.setModel(dtm);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RoomManagement.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

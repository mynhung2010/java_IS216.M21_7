    private void btnDoiMatKhauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDoiMatKhauActionPerformed
        // TODO add your handling code here:
        
        String taiKhoan = txtTenTaiKhoan.getText();
        String matKhaucu = txtMatKhauCu.getText();
        String matKhauMoi = txtMatKhauMoi.getText();
        
        String tenTaiKhoan = ShareData.useraccount.getTenDangNhapString();
        int index = 0;

        TaiKhoan tk = new TaiKhoan();
        try {
            if(taiKhoan.equals("") || matKhaucu.equals("") || matKhauMoi.equals(""))
                JOptionPane.showMessageDialog(this, "Thông tin chưa đẩy đủ"
                        , "Lỗi", JOptionPane.ERROR_MESSAGE); 
            else if(taiKhoan.length() != tenTaiKhoan.length())
                JOptionPane.showMessageDialog(this, "Tên tài khoản sai hoặc không tồn tại"
                        , "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE);                
            else if(matKhaucu.equals(matKhauMoi))
                JOptionPane.showMessageDialog(this, "Mật khẩu mới không thể trùng mật khẩu cũ"
                        , "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE); 
            else if(matKhaucu.equals(tk.getMatKhauHienTai(taiKhoan)))
                index = tk.DoiMatKhau(taiKhoan, matKhaucu, matKhauMoi);
            else
                JOptionPane.showMessageDialog(this, "Mật khẩu hiện tại không đúng"
                        , "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE); 
            if (index > 0) {
                JOptionPane.showMessageDialog(this, "Đổi thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Đổi thất bại");
            }
        }catch (SQLException | ClassNotFoundException  ex) {
            Logger.getLogger(DoiMatKhau.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnDoiMatKhauActionPerformed


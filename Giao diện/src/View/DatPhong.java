    public void loadDanhGia() throws SQLException{
        try {
            Room roomData = new Room();
            String arr[] = {"Mã phòng", "Điểm", "Bình luận", "Mã khách hàng"};
            List <Room> listRoom = roomData.getDanhGiaList(
                    (String) tbPhong.getModel().getValueAt(tbPhong.getSelectedRow(), 0));
            dtmDanhGia = new DefaultTableModel(arr,0);
            tbDanhGia.removeAll();
            tbDanhGia.setModel(dtmDanhGia);
            
            for (Room room : listRoom) {
                dtmDanhGia.addRow(new Object[]{
                    room.getMaPhong(), room.getDiem()
                        , room.getBinhLuan(), room.getMaKhachHang()
                });
            }
            
            dtmDanhGia.fireTableDataChanged();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ThongTinCacPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

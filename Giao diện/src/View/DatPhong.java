    /**
     * Tra cứu phòng
     * @throws SQLException 
     */
    public void traCuuPhong() throws SQLException{
        try {
            String loaiPhong = (String) cbbLoaiPhong.getSelectedItem();
            String chatLuong = (String) cbbChatLuong.getSelectedItem();
            Date ngDate = txtNgayDat.getDate();
            SimpleDateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            String ngayString = df.format(ngDate);
            
            Room roomData = new Room();
            String arr[] = {"Mã phòng", "Loại phòng", "Chất lượng","Giá"};
            List <Room> listRoom = roomData.traCuuPhong(Integer.parseInt(loaiPhong), chatLuong, ngayString);
            DefaultTableModel dtm = new DefaultTableModel(arr,0);
            tbPhong.removeAll();
            tbPhong.setModel(dtm);
            
            for (Room room : listRoom) {
                dtm.addRow(new Object[]{
                    room.getMaPhong(), room.getLoaiPhong()
                        , room.getChatLuongPhong(),room.getGia()
                });
            }
            
            dtm.fireTableDataChanged();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ThongTinCacPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

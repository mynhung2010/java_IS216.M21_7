    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        String maPhongString = txtMaPhong.getText();
        String maKhachHang = txtMaKhachHang.getText();
        String Diem = txtDiem.getText();
        String DanhGia = txtDanhGia.getText();
        
        int index = 0;
        Room r = new Room();
        try {
            if(Diem.equals("") || DanhGia.equals(""))
                JOptionPane.showMessageDialog(this, "Thông tin chưa đẩy đủ"
                        , "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE);
            else if(ShareData.kh.getMaKhachHangString().equals(maKhachHang))
                index = r.SuaDanhGia(Float.parseFloat(Diem), DanhGia, maPhongString, maKhachHang);             
            else
                JOptionPane.showMessageDialog(this, "Bạn không thể sửa bài đánh giá của khách hàng khác"
                    , "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE);   
            
            
            if(index > 0){
                JOptionPane.showMessageDialog(this, "Sửa thành công");
                loadDanhGia();
            }
            else
                JOptionPane.showMessageDialog(this, "Sửa thất bại");
        } catch (HeadlessException | ClassNotFoundException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(this, e.toString()
                        , "Lỗi", JOptionPane.ERROR_MESSAGE);           
        }
    }//GEN-LAST:event_btnSuaActionPerformed


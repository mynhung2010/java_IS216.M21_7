    /**
     * Hàm biểu diễn thông tin lên text field
     * @param row 
     */
    private void displayToTextField(int row){
        txtMaVatDung.setEnabled(false);
        txtMaVatDung.setText((String) tbThongTinVatDung.getModel().getValueAt(row, 0));
        
        txtTenVatDung.setText((String) tbThongTinVatDung.getModel().getValueAt(row, 1));
        
        txtSoLuong.setText((String) tbThongTinVatDung.getModel().getValueAt(row, 2));
        
        String tinhTrang = (String) tbThongTinVatDung.getModel().getValueAt(row, 3);
        
        switch (tinhTrang) {
            case "Tốt" -> cbbTinhTrang.setSelectedIndex(0);
            case "Hư hỏng" -> cbbTinhTrang.setSelectedIndex(1);
            default -> cbbTinhTrang.setSelectedIndex(2);
        }
        
        txtMaPhong.setEnabled(false);
        txtMaPhong.setText((String) tbThongTinVatDung.getModel().getValueAt(row, 4));
    }
    
    

    /**
     * Hàm load thông tin lên table
     * @throws SQLException 
     */
    public void loadData() throws SQLException{
        try {
            VatDung vatDungData = new VatDung();
            String arr[] = {"Mã vật dụng", "Tên vật dụng", "Số lượng", "Tình trạng", "Mã phòng"};
            List <VatDung> listVatDung = vatDungData.getFullData();
            DefaultTableModel dtm = new DefaultTableModel(arr,0);
            tbThongTinVatDung.removeAll();
            tbThongTinVatDung.setModel(dtm);
            
            for (VatDung vd : listVatDung) {
                dtm.addRow(new Object[]{
                    vd.getMaVatDung(), vd.getTenVatDung()
                   ,vd.getSoLuong(), vd.getTinhTrang()
                   ,vd.getMaPhong()
                });
            }
            
            dtm.fireTableDataChanged();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ThongTinCacPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

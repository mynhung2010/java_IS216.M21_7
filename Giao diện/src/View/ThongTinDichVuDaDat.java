    private void btnSuaDichVuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaDichVuActionPerformed
        // TODO add your handling code here:
        String maPhong = txtMaPhong.getText();
        String maHoaDon = txtMaHoaDon.getText();
        String maDichVu = txtMaDichVu.getText();
        String ghiChu  = txtGhiChu.getText();
        
        
        int index = 0;
        DichVu dv = new DichVu();
        
        try {
            
            if(ghiChu.equals(""))
                JOptionPane.showMessageDialog(this, "Không thể để trống ghi chú"
                    , "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE); 
            else
                index = dv.suaDichVuDaDat(maPhong, maHoaDon, maDichVu, ghiChu);
            
            if(index > 0){
                JOptionPane.showMessageDialog(this, "Sửa thành công");
                loadData();
            }
            else {
                JOptionPane.showMessageDialog(this, "Sửa thất bại");
            }
            
        } catch (HeadlessException | ClassNotFoundException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.toString()); 
        }
        
    }//GEN-LAST:event_btnSuaDichVuActionPerformed

    
    /**
     * Tìm các dịch vụ đã được đặt
     * @throws SQLException 
     */
    private void loadData() throws SQLException{
        try {
            DichVu dvData = new DichVu();
            String arr[] = {"Mã hóa đơn", "Mã phòng", "Mã dịch vụ", "Ghi chú"};
            List <DichVu> listDichVu = dvData.DichVuDaDat();
            DefaultTableModel dtm = new DefaultTableModel(arr,0);
            tbThongTinDichVuDaDat.removeAll();
            tbThongTinDichVuDaDat.setModel(dtm);
            
            for (DichVu dv : listDichVu) {
                dtm.addRow(new Object[]{
                   dv.getMaHoaDon()
                  ,dv.getMaPhong(), dv.getMaDichVuString()
                  ,dv.getGhiChu()
                });
            }
            
            dtm.fireTableDataChanged();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ThongTinCacPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


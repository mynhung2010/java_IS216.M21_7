    /**
     * Hàm tìm dịch vụ đã đặt
     * @throws SQLException 
     */
    private void FindDichVu() throws SQLException, ClassNotFoundException{
        String maPhong = txtTimKiem.getText();
        try {
            DichVu dvData = new DichVu();
            String arr[] = {"Mã hóa đơn", "Mã phòng", "Mã dịch vụ", "Ghi chú"};
            List <DichVu> listDichVu = dvData.TimDichVuDaDat(maPhong);
            DefaultTableModel dtm = new DefaultTableModel(arr,0);
            tbThongTinDichVuDaDat.removeAll();
            tbThongTinDichVuDaDat.setModel(dtm);
            
            for (DichVu dv : listDichVu) {
                dtm.addRow(new Object[]{
                   dv.getMaHoaDon()
                  ,dv.getMaPhong(), dv.getMaDichVuString()
                  ,dv.getGhiChu()
                });
            }
            
            dtm.fireTableDataChanged();
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }
    }
    
    

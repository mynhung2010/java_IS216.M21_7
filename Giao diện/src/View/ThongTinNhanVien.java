    public void TimKiemNhanVien() throws SQLException{
        String EmpName_In = txtTenTimKiem.getText();
        try {
            NhanVien nvData = new NhanVien();
            String arr[] = {"Mã nhân viên", "Họ tên", "SĐT","Mã chức vụ",
                    "Ngày sinh" , "Giới tính", "Ngày vào làm", "Email", "Mã tài khoản"};
            List <NhanVien> listNhanVien = nvData.timKiemNhanVien(EmpName_In);
            DefaultTableModel dtm = new DefaultTableModel(arr,0);
            tbThongTinNhanVien.removeAll();
            tbThongTinNhanVien.setModel(dtm);
            for (NhanVien nv : listNhanVien) {
                dtm.addRow(new Object[]{
                   nv.getMaNhanVien(), nv.getTenNhanVien()
                  ,nv.getSĐT(),nv.getMaChucVu()
                  ,nv.getNgaySinh(), nv.getGioiTinh()
                  ,nv.getNgayVaoLam(), nv.getEmail()
                  ,nv.getMaTaiKhoan()
                });
            }
            
            dtm.fireTableDataChanged();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ThongTinCacPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

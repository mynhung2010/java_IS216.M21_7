    /**
     * Tìm thông tin chức vụ
     * @throws SQLException 
    */
    private void TimChucVu() throws SQLException{
        String PosNo_In = txtTraCuuMa.getText();
        try {
            ChucVu cvData = new ChucVu();
            String arr[] = {"Mã chức vụ", "Tên chức vụ", "Mã nhân viên"};
            List <ChucVu> listChucVu = cvData.TimChucVu(PosNo_In);
            DefaultTableModel dtm = new DefaultTableModel(arr,0);
            tbThongTinChucVu.removeAll();
            tbThongTinChucVu.setModel(dtm);
            
            for (ChucVu cv : listChucVu) {
                dtm.addRow(new Object[]{
                   cv.getMaChucVu(), cv.getTenChucVu()
                  ,cv.getMaNhanVien()
                });
            }
            
            dtm.fireTableDataChanged();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ThongTinCacPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

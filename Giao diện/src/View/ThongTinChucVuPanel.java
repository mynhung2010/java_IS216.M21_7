    private void btnSuaChucVuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaChucVuActionPerformed
        // TODO add your handling code here:
        
        String maChucVu = txtMaChucVu.getText();
        
        String tenChucVu = txtTenChucVu.getText();
        
        String maNhanVien = txtMaNhanVien.getText();
        
        int index = 0;
        ChucVu cv = new ChucVu();
        
        try {
            index = cv.SuaChucVu(maChucVu, tenChucVu, maNhanVien);
            
            if(index > 0){
                JOptionPane.showMessageDialog(this, "Sửa thành công");
                loadData();
            }
            else
                JOptionPane.showMessageDialog(this, "Sửa thất bại");
        } catch (HeadlessException | ClassNotFoundException | SQLException e) {
        }
        
    }//GEN-LAST:event_btnSuaChucVuActionPerformed

    private void btnXoaChucVuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaChucVuActionPerformed
        // TODO add your handling code here:
        int ret = JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa","Xóa chức vụ",JOptionPane.YES_NO_OPTION);
        if (ret == JOptionPane.YES_OPTION){
            String maChucVu = txtMaChucVu.getText();

            int index = 0;

            ChucVu cv = new ChucVu();

            try {

                if(maChucVu.equals(""))
                    JOptionPane.showMessageDialog(this, "Không thể để trống mã chức vụ"
                        , "Lỗi nhập liệu", JOptionPane.ERROR_MESSAGE); 
                else
                    index = cv.XoaChucVu(maChucVu);

                if(index > 0){
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    loadData();
                }
                else
                    JOptionPane.showMessageDialog(this, "Xóa thất bại");

            } catch (HeadlessException | ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(this, "Mã chức vụ không tồn tại");
            }
        }          
    }//GEN-LAST:event_btnXoaChucVuActionPerformed

    

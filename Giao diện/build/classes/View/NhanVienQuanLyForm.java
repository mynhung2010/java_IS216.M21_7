    private void menuXuatHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuXuatHoaDonActionPerformed
        try {
            Connection con = ConnectionUtils.getMyConnection();

            String file = "src/Source/DoanhThu.jrxml";

            JasperReport jReport = JasperCompileManager.compileReport(file);
            
            JasperPrint jPrint = JasperFillManager.fillReport(jReport, new HashMap(), con);
            
            JasperViewer.viewReport(jPrint, false);
            
            JasperExportManager.exportReportToPdfFile(jPrint, file);
            
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString()
                        , "Lỗi", JOptionPane.ERROR_MESSAGE); 
        }
    }//GEN-LAST:event_menuXuatHoaDonActionPerformed


    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
        // TODO add your handling code here:
        
        String maDatPhong = (String) tbDatPhong.getModel().getValueAt(tbDatPhong.getSelectedRow(), 0);
        
        if(maDatPhong.equals(""))
            JOptionPane.showMessageDialog(this, "Mã đặt phòng trống", "Lỗi", JOptionPane.ERROR_MESSAGE);
        else if(thanhToan == 1){
            JOptionPane.showMessageDialog(this, "Bạn đã thanh toán rồi", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        else{
            thanhToan = 1;
            int index = 0;
            KhachHang kh = new KhachHang();
            
            try {
                index = kh.thanhToan(maDatPhong);
                
                if(index > 0)
                    JOptionPane.showMessageDialog(this, "Thanh toán thành công");
                else
                    JOptionPane.showMessageDialog(this, "Thanh toán thất bại");
            } catch (HeadlessException | ClassNotFoundException | SQLException e) {
                JOptionPane.showMessageDialog(this, e.toString(), "Lỗi", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnThanhToanActionPerformed

    
    public void loadData() throws SQLException{
        try {
            KhachHang khData = new KhachHang();
            String arr[] = {"Mã đặt phòng","Mã phòng", "Loại phòng", "Chất lượng","Giá"};
            List <Room> listRoom = khData.thongTinDatPhong(ShareData.kh.getMaKhachHangString());
            DefaultTableModel dtm = new DefaultTableModel(arr,0);
            tbDatPhong.removeAll();
            tbDatPhong.setModel(dtm);
            
            for (Room room : listRoom) {
                dtm.addRow(new Object[]{
                    room.getMaDatPhong()
                    ,room.getMaPhong(), room.getLoaiPhong()
                        , room.getChatLuongPhong(),room.getGia()
                });
            }
            
            dtm.fireTableDataChanged();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ThongTinCacPhong.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
